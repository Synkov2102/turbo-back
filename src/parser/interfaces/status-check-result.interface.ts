import { Car } from '../../schemas/car.schema';

/**
 * Результат массовой проверки статусов объявлений
 */
export interface StatusCheckResult {
  /** Список проверенных автомобилей */
  checked: Car[];
  /** Статистика проверки */
  stats: {
    /** Общее количество проверенных объявлений */
    total: number;
    /** Количество объявлений со статусом active */
    active: number;
    /** Количество проданных объявлений */
    sold: number;
    /** Количество удаленных объявлений */
    removed: number;
    /** Количество объявлений с неизвестным статусом */
    unknown: number;
    /** Количество объявлений, у которых изменился статус */
    statusChanged: number;
  };
  /** Список автомобилей, у которых изменился статус */
  changed: Car[];
}
