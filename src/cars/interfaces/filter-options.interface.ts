/**
 * Интерфейс для опций фильтров автомобилей
 * Используется для ответа эндпоинта /cars/filters/options
 */
export interface FilterOptions {
  /** Список уникальных брендов */
  brands: string[];

  /** Список уникальных городов */
  cities: string[];

  /** Список уникальных типов трансмиссий */
  transmissions: string[];

  /** Список уникальных моделей (всех брендов) */
  models: string[];

  /** Минимальный год выпуска среди всех автомобилей */
  minYear: number;

  /** Максимальный год выпуска среди всех автомобилей */
  maxYear: number;

  /** Минимальная цена среди всех автомобилей */
  minPrice: number;

  /** Максимальная цена среди всех автомобилей */
  maxPrice: number;

  /** Минимальный объем двигателя среди всех автомобилей */
  minEngineVolume: number;

  /** Максимальный объем двигателя среди всех автомобилей */
  maxEngineVolume: number;
}
